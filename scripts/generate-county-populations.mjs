#!/usr/bin/env node
/**
 * Fetches Census co-est2023 county population CSV and emits county-populations.ts
 * with COUNTY_POPULATIONS: Record<string, number> (5-digit FIPS -> population).
 * Run: node scripts/generate-county-populations.mjs
 */
const CSV_URL =
  "https://www2.census.gov/programs-surveys/popest/datasets/2020-2023/counties/totals/co-est2023-alldata.csv";
const OUT_PATH = "src/app/components/data/county-populations.ts";

import { createWriteStream } from "fs";
import { mkdir } from "fs/promises";

const res = await fetch(CSV_URL);
if (!res.ok) throw new Error(`Census CSV fetch failed: ${res.status}`);
const text = await res.text();
const lines = text.split(/\r?\n/);
const header = lines[0].split(",");
const idx = (name) => {
  const i = header.indexOf(name);
  if (i === -1) throw new Error(`Column not found: ${name}`);
  return i;
};
const iSumLev = idx("SUMLEV");
const iState = idx("STATE");
const iCounty = idx("COUNTY");
const iPop = idx("POPESTIMATE2023");

const out = {};
for (let i = 1; i < lines.length; i++) {
  const row = lines[i].split(",");
  if (row.length < Math.max(iSumLev, iState, iCounty, iPop) + 1) continue;
  if (row[iSumLev] !== "050") continue; // county level only
  const state = String(row[iState]).padStart(2, "0");
  const county = String(row[iCounty]).padStart(3, "0");
  const fips = state + county;
  const pop = parseInt(row[iPop], 10);
  if (Number.isNaN(pop)) continue;
  out[fips] = pop;
}

const dir = "src/app/components/data";
await mkdir(dir, { recursive: true });
const stream = createWriteStream(OUT_PATH, "utf8");
stream.write(
  "// County population estimates (Census POPESTIMATE2023). 5-digit FIPS (state + county).\n"
);
stream.write("// Generated by scripts/generate-county-populations.mjs\n\n");
stream.write("export const COUNTY_POPULATIONS: Record<string, number> = {\n");
const entries = Object.entries(out).sort(([a], [b]) => a.localeCompare(b));
for (const [fips, pop] of entries) {
  stream.write(`  "${fips}": ${pop},\n`);
}
stream.write("};\n");
stream.end();
console.log(`Wrote ${OUT_PATH} with ${entries.length} counties.`);
await new Promise((r) => stream.on("finish", r));
